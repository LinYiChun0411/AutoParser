import POJO.DD;

public class ISP {

    private String name;
    private String support;
    private DD dd01 = null;
    private DD dd02 = null;

    public String getSupport() {
        return support;
    }

    public void setSupport(String support) {
        this.support = support;
    }
    public DD getDd01() {
        return dd01;
    }

    public void setDd01(DD dd01) {
        this.dd01 = dd01;
    }

    public DD getDd02() {
        return dd02;
    }

    public void setDd02(DD dd02) {
        this.dd02 = dd02;
    }


    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }






}
